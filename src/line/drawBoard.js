import {
    Mesh,
    BoxGeometry,
    MeshNormalMaterial,
    Vector2,
    Vector3,
    Plane,
} from 'three';
import { Line2 } from 'three/examples/jsm/lines/Line2.js';
import { LineGeometry } from 'three/examples/jsm/lines/LineGeometry.js';
import { LineMaterial } from 'three/examples/jsm/lines/LineMaterial.js';
import {
    initRenderer,
    initOrthographicCamera,
    initCustomGrid,
    initAxesHelper,
    initOrbitControls,
    initScene,
    initGUI,
    resize,
    dataToVec3
} from '../lib/tools/index.js';

window.onload = () => {
    init();
};

function init() {
    const renderer = initRenderer();
    const camera = initOrthographicCamera();
    camera.up.set(0, 0, 1);
    camera.updateProjectionMatrix();

    const scene = initScene();
    initAxesHelper(scene);
    renderer.setClearColor(0xffffff);
    initCustomGrid(scene);

    const controls = initOrbitControls(camera, renderer.domElement);
    const mesh = new Mesh(new BoxGeometry(3, 3, 3), new MeshNormalMaterial());
    scene.add(mesh);

    const cameraDir = new Vector3();
    const renderSize = new Vector2(window.innerWidth, window.innerHeight);
    const centerPoint = new Vector3();

    const plane = new Plane();
    const lineColorsArray = ['#ff0000', '#ed7b2f', '#ffff00', '#00a870', '#0594fa', '#ffffff', '#000000'];
    const drawTypeArray = ['PaintBrush', 'Line', 'Rect', 'Circle'];

    function render() {
        controls.update();
        renderer.render(scene, camera);
        requestAnimationFrame(render);
    }
    render();

    const params = {
        draw: startDraw,
        exit: endDraw,
        drawType: 'PaintBrush',
        lineWidth: 1,
        lineColor: '#000000',
    }
    const tmpLine = new Line2(new LineGeometry(), new LineMaterial({ color: params.lineColor, linewidth: params.lineWidth }));
    scene.add(tmpLine);

    let tmpPoints = [];
    const tmpMousePos = [];

    function startDraw() {
        controls.enabled = false;
        updateCameraInfo();
        renderer.domElement.addEventListener('mousedown', onMouseDown);
        renderer.domElement.addEventListener('mouseup', onMouseUp);
    }

    function updateRecord(x, y) {
        const v = new Vector2(x, y);
        if (params.drawType === 'PaintBrush') {
            tmpMousePos.push(v);
        } else {
            if (tmpMousePos.length === 0) {
                tmpMousePos.push(v);
            } else {
                tmpMousePos[1] = v;
            }
        }
        let position = tmpMousePos;

        if (params.drawType === 'Rect' && tmpMousePos.length > 1) {
            position = getRectCorners(tmpMousePos[0], tmpMousePos[1])
        } else if (params.drawType === 'Circle' && tmpMousePos.length > 1) {
            position = getCircleCorners(tmpMousePos[0].clone(), tmpMousePos[1].clone(), 32)
        }
        tmpPoints = position.map(pos => project(pos));
    }

    function onMouseDown(e) {
        tmpPoints.length = 0;
        tmpMousePos.length = 0;
        updateRecord(e.clientX, e.clientY);
        renderer.domElement.addEventListener('mousemove', onMouseMove);
    }

    function onMouseMove(e) {
        updateRecord(e.clientX, e.clientY);
        updateView();
    }

    function project(screenPoint) {
        const v = new Vector3(
            (screenPoint.x / renderSize.x) * 2 - 1,
            -(screenPoint.y / renderSize.y) * 2 + 1,
            0
        );
        v.unproject(camera);
        const res = plane.projectPoint(v, new Vector3())
        return res;
    }

    function onMouseUp(e) {
        updateRecord(e.clientX, e.clientY);
        renderer.domElement.removeEventListener('mousemove', onMouseMove);
        addLine();
    }

    function endDraw() {
        controls.enabled = true;
        renderer.domElement.removeEventListener('mousedown', onMouseDown);
        renderer.domElement.removeEventListener('mouseup', onMouseUp);
    }

    function updateCameraInfo() {
        cameraDir.copy(camera.position).sub(controls.target).normalize();
        centerPoint.copy(cameraDir).multiplyScalar(camera.position.distanceTo(controls.target) / 2);
        plane.setFromNormalAndCoplanarPoint(cameraDir, centerPoint);
    }


    function updateView() {
        tmpLine.geometry.dispose();
        tmpLine.geometry = new LineGeometry().setFromPoints(tmpPoints);
        tmpLine.material.color.set(params.lineColor);
        tmpLine.material.linewidth = params.lineWidth;
    }

    function addLine() {
        const line = new Line2(new LineGeometry(), new LineMaterial({ color: params.lineColor, linewidth: params.lineWidth }));
        line.geometry.setFromPoints(tmpPoints);
        const points = [];
        tmpPoints.forEach(({ x, y, z }) => {
            points.push(x, y, z);
        });
        line.userData.points = points;
        scene.add(line);
    }

    resize(renderer, camera, (w, h) => renderSize.set(w, h));

    const gui = initGUI();
    gui.add(params, 'draw');
    gui.add(params, 'exit');
    gui.add(params, 'drawType', drawTypeArray);
    const lineStyleFolder = gui.addFolder('lineStyle');
    lineStyleFolder.add(params, 'lineWidth', { light: 1, middle: 4, thick: 6 });
    const colorController = lineStyleFolder.add(params, 'lineColor', lineColorsArray).onChange(() => {
        colorController.$display.style.backgroundColor = colorController.object.lineColor;
    });
    colorController.$display.style.backgroundColor = colorController.object.lineColor;
    colorController.$select.childNodes.forEach((item, i) => {
        item.style.backgroundColor = lineColorsArray[i];
    })

    // eslint-disable-next-line no-use-before-define
    const wizard = new Line2(new LineGeometry().setFromPoints(dataToVec3(data)), new LineMaterial({ color: params.lineColor, linewidth: 6 }));
    scene.add(wizard);
}

function getRectCorners(p1, p2) {
    return [
        { x: p1.x, y: p1.y }, // A
        { x: p2.x, y: p1.y }, // B
        { x: p2.x, y: p2.y }, // C
        { x: p1.x, y: p2.y }, // D
        { x: p1.x, y: p1.y }, // A
    ];
}

function getCircleCorners(p1, p2, segments = 32) {
    const cx = (p1.x + p2.x) / 2;
    const cy = (p1.y + p2.y) / 2;
    const rx = Math.abs(p2.x - p1.x) / 2;
    const ry = Math.abs(p2.y - p1.y) / 2;

    const points = [];
    for (let i = 0; i <= segments; i++) {
        const theta = (i / segments) * 2 * Math.PI;
        points.push({
            x: cx + rx * Math.cos(theta),
            y: cy + ry * Math.sin(theta),
        });
    }
    return points;
}

