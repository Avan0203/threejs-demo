  const result = [];
  const { length } = pointList;
  for (let i = 0, j = length - 1; i < length; j = i++) {
    const q = i === length - 1 ? 0 : i + 1;
    result.push(innerPoints(pointList[j], pointList[i], pointList[q], width));
  }

  return result;
